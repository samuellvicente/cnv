def rem(p, r):
    newp = p
    l = len(p)
    tot = l ** 2
    step = tot // r
    k = 0
    for n in range(0, tot, step):
        i = n // l
        j = n % l
        newp[i][j] = "0"
        k = k + 1
        if k == r:
            break
    return newp

